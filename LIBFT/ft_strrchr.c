#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int		i;
	char	*str;

	i = ft_strlen(s);
	str = (char *)s;
	while (i > 0 && str[i] != c)
		i--;
	if (str[i] == (char)c)
		return (&str[i]);
	return (0);
}
